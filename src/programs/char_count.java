package programs;

public class char_count 
{

	public static void main(String[] args)
	{
		String s = "xavier";    
        int count = 0;    
               
        for(int i = 0; i < s.length(); i++) 
        {    
             
                count++;    
        }    
             
        System.out.println("count: " + count);    

	}

}
