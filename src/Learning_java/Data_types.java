package Learning_java;

public class Data_types
{

	public static void main(String[] args)
	{
		int number1=123;
		short number2=23;
		byte number3=12;
		long number4=1234545645645l;
		float number5=25.234f;
		char c='T';
		String Name="XAVIER";
		
		System.out.println("Integer number is "+number1+"\n");
		System.out.println("Short type number is "+number2+"\n");
		System.out.println("byte number is "+number3+"\n");
		System.out.println("long number is "+number4+"\n");
		System.out.println("float number is "+number5+"\n");
		System.out.println("char  is "+c+"\n");
		System.out.println("String number is "+Name+"\n");
		

	}

}
